import sqlite3


################################################################################
# Table creation functions
################################################################################

def create_tag_table(db_file_path):
    with sqlite3.connect(db_file_path) as conn:
        cur = conn.cursor()
        cur.execute('''CREATE TABLE "tag" (
	"id"	INTEGER,
	"name"	TEXT UNIQUE,
	PRIMARY KEY("id")
)''')
        conn.commit()




